    private void btnSeleccionarOdontologoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarOdontologoActionPerformed
        // obtenemos el odontologo seleccionado
        Odontologo odontologo = (Odontologo) comboOdontologo.getSelectedItem();
        
        // le pasamos al modelo de la tabla el detalle de la definicion de agenda
        definicionAgendaModel.setDetalle(odontologo.getDefinicionAgendaVigente().getDetalleDefinicion());
        
        // actualizamos los datos en la tabla
        definicionAgendaModel.fireTableDataChanged();
    }//GEN-LAST:event_btnSeleccionarOdontologoActionPerformed

    private void btnGenerarAgendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarAgendaActionPerformed
        // obtenemos el odontolog, mes y el año seleccionados
        Odontologo odontologo = (Odontologo) comboOdontologo.getSelectedItem();
        
        // parseamos el mes
        Integer mes = null;
        SimpleDateFormat sdf = new SimpleDateFormat("MMMM");
        try {
            Calendar cal = Calendar.getInstance();
            cal.setTime(sdf.parse(comboMes.getSelectedItem().toString().toLowerCase()));
            
            mes = cal.get(Calendar.MONTH);
        }
        catch (ParseException ex) {
            System.out.println("Imposible obtener un mes válido de " + comboMes.getSelectedItem().toString().toLowerCase());
        }
        
        // obtenemos el anio
        Integer anio = Integer.parseInt(comboAnio.getSelectedItem().toString());
        
        // y delegamos la generacion de la agenda al gestor
        gestor.generarAgenda (odontologo, mes, anio);
    }//GEN-LAST:event_btnGenerarAgendaActionPerformed

